class Solution {
public:
    bool canPartition(vector<int>& nums) {
        int sum = 0, target = 0;
        for (int n : nums) {
            sum += n;
        }
        if (sum % 2) {
            return false;
        }
        target = sum / 2;
        vector<int> dp(10001, 0);
        for (int i = 0; i < nums.size(); i++) {
            for (int j = target; j >= nums[i]; j--) {
                dp[j] = max(dp[j], dp[j - nums[i]] + nums[i]);
            }
        }
        return dp[target] == target;
    }
};
